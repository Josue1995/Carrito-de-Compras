     */
    public function store(Request $request)
    {
        $catalogo = new Catalogo($request->all());
        $catalogo->empresa_id = $request->get('empresa_id');
        $catalogo->save();
        if(Auth::user()->rol == 'Empresa'){
            return redirect('/home');
        }
        return redirect('home');
        
    }

